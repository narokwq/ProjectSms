package com.br.model;

public class Grupo {
	public String grupo;
	public String nome;
	
	public Grupo() {
	}
	
	public Grupo(String grupo, String nome) {
		super();
		this.grupo = grupo;
		this.nome = nome;
	}

	public String getGrupo() {
		return grupo;
	}

	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	

	
}
